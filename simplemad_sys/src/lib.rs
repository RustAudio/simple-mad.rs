#[derive(Clone, Copy)]
#[repr(C)]
pub struct MadFixed32 {
    /* Fixed-point format: 0xABBBBBBB
       A == whole part (sign + 3 bits)
       B == fractional part (28 bits)
    */
    value: i32,
}

impl MadFixed32 {
    pub fn new(v: i32) -> MadFixed32 {
        MadFixed32 {
            value: v,
        }
    }

    pub fn to_i32(&self) -> i32 {
        if self.value > i32::max_value() / 8 {
            i32::max_value()
        } else if self.value < i32::min_value() / 8 {
            i32::min_value()
        } else {
            self.value * 8
        }
    }

    pub fn to_f32(&self) -> f32 {
        // Divide by 2^28
        (self.value as f32) / 268435456.0
    }

    pub fn to_f64(&self) -> f64 {
        // Divide by 2^28
        (self.value as f64) / 268435456.0
    }
}

impl Default for MadFixed32 {
    fn default() -> MadFixed32 {
        MadFixed32 {value: 0}
    }
}

impl From<i32> for MadFixed32 {
    fn from(v: i32) -> MadFixed32 {
        MadFixed32 {value: v / 8}
    }
}

impl From<f32> for MadFixed32 {
    fn from(v: f32) -> MadFixed32 {
        MadFixed32 {
            value: (v * 268435456.0) as i32,
        }
    }
}

impl From<f64> for MadFixed32 {
    fn from(v: f64) -> MadFixed32 {
        MadFixed32 {
            value: (v * 268435456.0) as i32,
        }
    }
}

impl fmt::Debug for MadFixed32 {
    fn fmt(&self, f: &mut fmt::Formatter) -> fmt::Result {
        write!(f, "{}", self.to_f32())
    }
}

/// libmad callbacks return MadFlow values, which are used to control the decoding process
#[allow(dead_code)]
#[derive(Debug, Clone)]
#[repr(C)]
pub enum MadFlow {
    /// continue normally
    Continue = 0x0000,

    /// stop decoding normally
    Stop = 0x0010,

    /// stop decoding and signal an error
    Break = 0x0011,

    /// ignore the current frame
    Ignore = 0x0020,

}

/// Errors generated by libmad
#[derive(Debug, Clone, PartialEq)]
#[repr(C)]
pub enum MadError {
    /// no error
    None = 0x0000,

    /// input buffer too small (or eof)
    BufLen = 0x0001,

    /// invalid (null) buffer pointer
    BufPtr = 0x0002,

    /// not enough memory
    NoMem = 0x0031,

    /// lost synchronization
    LostSync = 0x0101,

    /// reserved header layer value
    BadLayer = 0x0102,

    /// forbidden bitrate value
    BadBitRate = 0x0103,

    /// reserved sample frequency value
    BadSampleRate = 0x0104,

    /// reserved emphasis value
    BadEmphasis = 0x0105,

    /// crc check failed
    BadCRC = 0x0201,

    /// forbidden bit allocation value
    BadBitAlloc = 0x0211,

    /// bad scalefactor index
    BadScaleFactor = 0x0221,

    /// bad bitrate/mode combination
    BadMode = 0x0222,

    /// bad frame length
    BadFrameLen = 0x0231,

    /// bad big_values count
    BadBigValues = 0x0232,

    /// reserved block_type
    BadBlockType = 0x0233,

    /// bad scalefactor selection info
    BadScFSI = 0x0234,

    /// bad main_data_begin pointer
    BadDataPtr = 0x0235,

    /// bad audio data length
    BadPart3Len = 0x0236,

    /// bad huffman table select
    BadHuffTable = 0x0237,

    /// huffman data overrun
    BadHuffData = 0x0238,

    /// incompatible block_type for joint stereo
    BadStereo = 0x0239,
}

impl Default for MadError {
    fn default() -> MadError {
        MadError::None
    }
}

